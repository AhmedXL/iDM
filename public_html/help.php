<h3><a name="other" id="other">Other</a></h3>
<ul>
<li>!on option - Turns on the commands that you want. See below for more info.</li>
<li>!off option - Disables certain attacks in the channel. See below for more info.</li>
</ul>
<ul>
<li>For each option you can specify a weapon such as sgs or dh and to disable more then one at a time use commas between them without spaces sgs,dh</li>
<li>You can also disable healing attacks by disabling -h</li>
<li>To view which weapons are disabled use -L as the option</li>
<li>To turn all weapons back on use all</li>
</ul>
