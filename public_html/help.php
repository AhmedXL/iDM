<h2>iDM Information</h2>
<h3><a name="bl" id="bl">Channel Blacklists and Bot Ignores:</a></h3>
<h4>General Information</h4>
<ul>
<li>Blacklists and ignores are dealt by either admins or support staff</li>
<li>The only person who can remove the blacklist or ignore is the person who issued it</li>
<li>In order to get either a blacklist or ignore removed you must appeal, talking to the person who issued it will NOT get it removed</li>
</ul>
<h4>Channel Blacklists</h4>
<p>What happens when your channel gets blacklisted is you are unable to have a bot join that channel unless the blacklist is removed.
In order to get the blacklist removed you need to post an <a href="http://r.idm-bot.com/bl" class="postlink">appeal</a> which ONLY a channel owner or op can do, if you are a normal users in this channel you are unable to get it removed.</p>
<h4>Ignores</h4>
<p>If you are ignored from the bot you have done an action which breaks one of our rules.
These along with blacklists must be <a href="http://r.idm-bot.com/bl" class="postlink">appealed.</a>
The issuer can either remove it if the appeal is accepted at that moment or can remove it after a period of time depending on the severity of your actions.</p>

