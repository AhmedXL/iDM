<div class="c8"><span class="c18"><span class="c17"><a name="general" id="general">General</a></span>:</span><br />
<ul>
<li><span class="c6">!dm</span> - Start a DM</li>
<li><span class="c6">!gwd #</span> - Start a Gwd team (# = Armadyl/Corporeal/Bandos/Zamorak/Saradomin)</li>
<li><span class="c6">!stake</span> - Start a staking DM (<span class="c14">Max: 5% of your money</span>)</li>
