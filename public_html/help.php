<h3><a name="clan" id="clan">Clan</a></h3>
<ul>
<li>startclan name - Starts a clan with the name you specify, no spaces allowed.</li>
<li>!addmem user - Sends an invite to the name you specify to join your clan</li>
<li>!delmem user - Removes a member from your clan</li>
<li>!joinclan name - Joins the clan you specify after an invite</li>
<li>!leaveclan - Leaves the clan you are currently in</li>
<li>!share on/off - Turns on or off lootshare, clan owner only.</li>
<li>!dmclan user - Shows information about the current clan you are in, name optional.</li>
</ul>

