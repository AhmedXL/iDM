<li><span class="c6">!on <span class="c14">option</span></span> - Turns on the commands that you want. See below for more info.</li>
<li><span class="c6">!off <span class="c14">option</span></span> - Disables certain attacks in the channel. See below for more info.</li>
</ul>
<br />
<ul>
<li>For each option you can specify a weapon such as <span class="c14">sgs</span> or <span class="c14">dh</span> and to disable more then one at a time use commas between them without spaces <span class="c14">sgs,dh</span></li>
<li>You can also disable healing attacks by disabling <span class="c14">-h</span></li>
<li>To view which weapons are disabled use <span class="c14">-L</span> as the option</li>
<li>To turn all weapons back on use <span class="c14">all</span></li>
</ul>
