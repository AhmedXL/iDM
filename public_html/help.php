<h2>Commands</h2>
<h3><a name="general" id="general">General</a></h3>
<ul>
<li>!dm - Start a DM</li>
<li>!gwd # - Start a Gwd team (# = Armadyl/Corporeal/Bandos/Zamorak/Saradomin)</li>
<li>!stake - Start a staking DM (<span class="c14">Max: 5% of your money</span>)</li>
<li>!enddm - Gives other players option to go or agree to end dm, if neither happens DM times out.</li>
<li>!money name - Shows your money, wins/losses, items and clan. Name is optional.</li>
<li>!top # - Shows the top # (Money) (# = 1 to 12)</li>
<li>!wtop # - Shows the top # (Wins) (# = 1 to 12)</li>
<li>!ltop # - Shows the top # (Losses) (# = 1 to 12)</li>
<li>!status - Shows the status of the current DM</li>
<li>!specpot - Restores your special bar to 100%</li>
<li>!max weapon - Displays the max hits of the item you specify</li>
<li>!hitchance weapon hit - Gives you a % chance to hit what you specify</li>
<li>!dmstats # - Shows the DM stats for a channel. (iDM[Stats] only)</li>
<li>!dmoverall - The overall DM top for channels, both cash and dms (iDM[Stats] only)</li>
<li>!dmdaily - The daily DM top for channels, both cash and dms (iDM[Stats] only)</li>
</ul>

